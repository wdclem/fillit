#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;

	ptr = NULL;
	while (1)
	{
		if (*s == c && ptr < s)
			ptr = (char *)s;
		if (*s == 0)
			return (ptr);
		s++;
	}
}
